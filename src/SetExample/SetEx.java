package SetExample;

import java.util.HashSet;
import java.util.Set;

public class SetEx {

	public static void main(String[] args) {
		Set<String> names = new HashSet<String>();
		
		names.add("Srinivas");
		names.add("Anita");
		names.add("Shripriya");
		names.add("Shrinika");
		
		System.out.println(names);
	}

}
